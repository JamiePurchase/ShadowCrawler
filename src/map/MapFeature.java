package map;

public class MapFeature
{
    private String name;
    private String type;
    private int posX, posY;
    
    public MapFeature(String name, String type, int posX, int posY)
    {
        this.name = name;
        this.type = type;
        this.posX = posX;
        this.posY = posY;
    }
    
}